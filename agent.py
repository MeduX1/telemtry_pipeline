def get_db():
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()

class GraphState(TypedDict):
    patient_id: str
    question: str
    heart_beat: float
    systolic_bp: float
    Patient_status: str
    llm_recommendation: str


    

def get_patient_structured_data(payload:GraphState):
    print(f"[System-log] fetching structured data for {payload['patient_id']}")

    db= SessionLocal() 
    patient_data = db.query(models.patienttelementery)\
    .filter(models.patienttelementery.patient_id == payload['patient_id'] ) \
    .first()
    if patient_data:
        return {'heart_beat':patient_data.heart_rate,'systolic_bp':patient_data.systolic_bp}
    else:
        return {'heart_beat':0, 'systolic_bp':0}
    
def get_patient_unstructured_data(payload:GraphState):
    print(f"[System-log] extracting the unstructured data about patient {payload['patient_id']}")
    response = collection.query(
        query_texts= [payload['question']],
        n_results=1,
        where = {'patient_id':payload['patient_id']} 
    )
    print('[System-log] Query is in progress')

    return {'Patient_status': response['documents'][0][0]}

