graph = StateGraph(GraphState)
graph.add_node('get_patient_structured_data',get_patient_structured_data)
graph.add_node('get_patient_unstructured_data',get_patient_unstructured_data)
graph.add_node('get_llm_recommendation',get_llm_recommendation)

